    <p class="bold">Epidurale lombaire par voie interépineuse :</p>
    <p>repérer l’apophyse épineuse de L4 sur un patient assis, habituellement sur la ligne horizontale joignant les deux crêtes iliaques. La palpation de la dépression située entre les épineuses visées est plus simple en légère lordose lombaire avant de replacer le patient en cyphose. Insérer l’aiguille horizontalement ou parfois sur un trajet légèrement ascendant en regard de cette dépression et en visant l’ombilic. Progresser doucement avec une pression sur la seringue remplie du produit d’infiltration jusqu’à passage du ligament jaune marqué par une perte de résistance et le passage du produit. Arrêter alors la progression de l’aiguille et terminer l’infiltration.</p>

